package chap06;

public class Printer2 {
	static void println(int intVar) {
		System.out.println(intVar);
	}

	static void println(boolean booleanVar) {
		System.out.println(booleanVar);
	}

	static void println(double doubleVar) {
		System.out.println(doubleVar);
	}

	static void println(String stringVar) {
		System.out.println(stringVar);
	}
}
